/* ── Apply translations function ── */
window.applyTranslations = function(lang) {
  var page = document.body.getAttribute('data-page') || 'shared';
  var trans = window.EDMN_TRANS;

  document.querySelectorAll('[data-t]').forEach(function(el) {
    var key = el.getAttribute('data-t');
    var ns  = el.getAttribute('data-ns') || page;

    var val = (trans[ns] && trans[ns][key] && trans[ns][key][lang]) ||
              (trans.shared && trans.shared[key] && trans.shared[key][lang]);

    if (val !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.textContent = val;
      }
    }
  });
};
